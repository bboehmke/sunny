func TestResponseValue_Bytes(t *testing.T) {
	ass := assert.New(t)

	value := ResponseValue{
		Class:     0x12,
		Code:      0x1234,
		Type:      0x12,
		Timestamp: 0x12345678,
	}
	ass.Equal([]byte{
		0x12,
		0x34, 0x12,
		0x12,
		0x78, 0x56, 0x34, 0x12,
	}, value.Bytes(0x00))

	value = ResponseValue{
		Class:     0x12,
		Code:      0x1234,
		Type:      0x10,
		Timestamp: 0x12345678,

		Values: []interface{}{
			"aaa",
		},
	}
	ass.Equal([]byte{
		0x12,
		0x34, 0x12,
		0x10,
		0x78, 0x56, 0x34, 0x12,

		0x61, 0x61, 0x61, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	}, value.Bytes(0x00))

	value = ResponseValue{
		Class:     0x12,
		Code:      0x1234,
		Type:      0x08,
		Timestamp: 0x12345678,

		Values: []interface{}{
			uint32(0x123456),
			uint8(0x12),
		},
	}
	ass.Equal([]byte{
		0x12,
		0x34, 0x12,
		0x08,
		0x78, 0x56, 0x34, 0x12,

		0x56, 0x34, 0x12, 0x01,
		0xfe, 0xff, 0xff, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}, value.Bytes(0x00))

	value = ResponseValue{
		Class:     0x12,
		Code:      0x1234,
		Type:      0x12,
		Timestamp: 0x12345678,

		Values: []interface{}{
			uint64(0x1234567812345678),
		},
	}
	ass.Equal([]byte{
		0x12,
		0x34, 0x12,
		0x12,
		0x78, 0x56, 0x34, 0x12,

		0x78, 0x56, 0x34, 0x12, 0x78, 0x56, 0x34, 0x12,
	}, value.Bytes(0x5400))

	value = ResponseValue{
		Class:     0x12,
		Code:      0x1234,
		Type:      0x00,
		Timestamp: 0x12345678,

		Values: []interface{}{
			uint32(0x12345678),
			uint8(0x12),
		},
	}
	ass.Equal([]byte{
		0x12,
		0x34, 0x12,
		0x00,
		0x78, 0x56, 0x34, 0x12,

		0x78, 0x56, 0x34, 0x12,
		0xff, 0xff, 0xff, 0xff,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}, value.Bytes(0x00))

	value = ResponseValue{
		Class:     0x12,
		Code:      0x1234,
		Type:      0x40,
		Timestamp: 0x12345678,

		Values: []interface{}{
			int32(-0x12345678),
			uint8(0x12),
		},
	}
	ass.Equal([]byte{
		0x12,
		0x34, 0x12,
		0x40,
		0x78, 0x56, 0x34, 0x12,

		0x88, 0xa9, 0xcb, 0xed,
		0x00, 0x00, 0x00, 0x80,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}, value.Bytes(0x00))
}

func TestResponseValue_Read(t *testing.T) {
	ass := assert.New(t)

	value := new(ResponseValue)

	n, err := value.Read([]byte{0x12, 0x34}, 0x00)
	ass.EqualError(err, "invalid ResponseValue - length 2")
	ass.Equal(0, n)

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x12,
		0x78, 0x56, 0x34, 0x12,
	}, 0x00)
	ass.NoError(err)
	ass.Equal(8, n)
	ass.Equal(uint8(0x12), value.Class)
	ass.Equal(uint16(0x1234), value.Code)
	ass.Equal(uint8(0x12), value.Type)
	ass.Equal(uint32(0x12345678), value.Timestamp)

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x10,
		0x78, 0x56, 0x34, 0x12,

		0x61, 0x61, 0x61, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	}, 0x00)
	ass.NoError(err)
	ass.Equal(40, n)
	ass.Equal(uint8(0x12), value.Class)
	ass.Equal(uint16(0x1234), value.Code)
	ass.Equal(uint8(0x10), value.Type)
	ass.Equal(uint32(0x12345678), value.Timestamp)
	ass.Len(value.Values, 1)
	ass.Equal("aaa", value.Values[0].(string))

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x08,
		0x78, 0x56, 0x34, 0x12,

		0x56, 0x34, 0x12, 0x01,
		0xfe, 0xff, 0xff, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}, 0x00)
	ass.NoError(err)
	ass.Equal(40, n)
	ass.Equal(uint8(0x12), value.Class)
	ass.Equal(uint16(0x1234), value.Code)
	ass.Equal(uint8(0x08), value.Type)
	ass.Equal(uint32(0x12345678), value.Timestamp)
	ass.Len(value.Values, 1)
	ass.Equal(uint32(0x123456), value.Values[0])

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x12,
		0x78, 0x56, 0x34, 0x12,

		0x78, 0x56, 0x34, 0x12, 0x78,
	}, 0x5400)
	ass.EqualError(err, "invalid ResponseValue - length 13")
	ass.Equal(8, n)

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x12,
		0x78, 0x56, 0x34, 0x12,

		0x78, 0x56, 0x34, 0x12, 0x78, 0x56, 0x34, 0x12,
	}, 0x5400)
	ass.NoError(err)
	ass.Equal(16, n)
	ass.Equal(uint8(0x12), value.Class)
	ass.Equal(uint16(0x1234), value.Code)
	ass.Equal(uint8(0x12), value.Type)
	ass.Equal(uint32(0x12345678), value.Timestamp)
	ass.Len(value.Values, 1)
	ass.Equal(uint64(0x1234567812345678), value.Values[0])

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x00,
		0x78, 0x56, 0x34, 0x12,

		0x78, 0x56, 0x34, 0x12,
		0xff, 0xff, 0xff, 0xff,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}, 0x00)
	ass.NoError(err)
	ass.Equal(28, n)
	ass.Equal(uint8(0x12), value.Class)
	ass.Equal(uint16(0x1234), value.Code)
	ass.Equal(uint8(0x00), value.Type)
	ass.Equal(uint32(0x12345678), value.Timestamp)
	ass.Len(value.Values, 1)
	ass.Equal(uint32(0x12345678), value.Values[0])

	n, err = value.Read([]byte{
		0x12,
		0x34, 0x12,
		0x40,
		0x78, 0x56, 0x34, 0x12,

		0x88, 0xa9, 0xcb, 0xed,
		0x00, 0x00, 0x00, 0x80,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}, 0x00)
	ass.NoError(err)
	ass.Equal(28, n)
	ass.Equal(uint8(0x12), value.Class)
	ass.Equal(uint16(0x1234), value.Code)
	ass.Equal(uint8(0x40), value.Type)
	ass.Equal(uint32(0x12345678), value.Timestamp)
	ass.Len(value.Values, 1)
	ass.Equal(int32(-0x12345678), value.Values[0])
}

