func TestDeviceData_Bytes(t *testing.T) {
	ass := assert.New(t)

	data := DeviceData{
		Control: 0x12,
		Destination: DeviceId{
			SusyID:       0x1234,
			SerialNumber: 0x12345678,
		},
		JobNumber: 0x12,
		Source: DeviceId{
			SusyID:       0x1234,
			SerialNumber: 0x12345678,
		},
		Status:      0x1234,
		PacketCount: 0x2143,
		PacketID:    0x0123,

		Command: 0x12,
		Object:  0x1234,

		Parameters: []uint32{
			0x12345678,
			0x12345678,
		},
	}

	ass.Equal([]byte{
		0x09,       // length
		0x12,       // control
		0x34, 0x12, // DstSusyID
		0x78, 0x56, 0x34, 0x12, // DstSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // SrcSusyID
		0x78, 0x56, 0x34, 0x12, // SrcSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // Status
		0x43, 0x21, // PacketCount
		0x23, 0x81, // PacketID | 0x8000
		0x12,       // Command
		0x02,       // Parameter count
		0x34, 0x12, // Object
		0x78, 0x56, 0x34, 0x12, // parameter 1
		0x78, 0x56, 0x34, 0x12, // parameter 2
	}, data.Bytes())

	data = DeviceData{
		Control: 0x12,
		Destination: DeviceId{
			SusyID:       0x1234,
			SerialNumber: 0x12345678,
		},
		JobNumber: 0x12,
		Source: DeviceId{
			SusyID:       0x1234,
			SerialNumber: 0x12345678,
		},
		Status:      0x1234,
		PacketCount: 0x2143,
		PacketID:    0x0123,

		Command: 0x12,
		Object:  0x1234,

		Parameters: []uint32{
			0x12345678,
		},

		Data: []byte{0x12, 0x34},
	}

	ass.Equal([]byte{
		0x08,       // length
		0x12,       // control
		0x34, 0x12, // DstSusyID
		0x78, 0x56, 0x34, 0x12, // DstSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // SrcSusyID
		0x78, 0x56, 0x34, 0x12, // SrcSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // Status
		0x43, 0x21, // PacketCount
		0x23, 0x81, // PacketID | 0x8000
		0x12,       // Command
		0x01,       // Parameter count
		0x34, 0x12, // Object
		0x78, 0x56, 0x34, 0x12, // parameter 1
		0x12, 0x34,
	}, data.Bytes())
}

func TestDeviceData_Read(t *testing.T) {
	ass := assert.New(t)

	data := new(DeviceData)

	ass.EqualError(data.Read([]byte{0x09, 0x12}),
		"invalid DeviceData - length 2")

	ass.EqualError(data.Read([]byte{
		0x05,       // length (invalid)
		0x12,       // control
		0x34, 0x12, // DstSusyID
		0x78, 0x56, 0x34, 0x12, // DstSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // SrcSusyID
		0x78, 0x56, 0x34, 0x12, // SrcSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // Status
		0x43, 0x21, // PacketCount
		0x23, 0x81, // PacketID | 0x8000
		0x12,       // Command
		0x02,       // Parameter count
		0x34, 0x12, // Object
		0x78, 0x56, 0x34, 0x12, // parameter 1
		0x78, 0x56, 0x34, 0x12, // parameter 2
	}), "invalid sma net2 data size. expected 20 - get 36")

	ass.NoError(data.Read([]byte{
		0x09,       // length (invalid)
		0x12,       // control
		0x34, 0x12, // DstSusyID
		0x78, 0x56, 0x34, 0x12, // DstSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // SrcSusyID
		0x78, 0x56, 0x34, 0x12, // SrcSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // Status
		0x43, 0x21, // PacketCount
		0x23, 0x81, // PacketID | 0x8000
		0x12,       // Command
		0x02,       // Parameter count
		0x34, 0x12, // Object
		0x78, 0x56, 0x34, 0x12, // parameter 1
		0x78, 0x56, 0x34, 0x12, // parameter 2
	}))

	ass.Equal(uint8(0x12), data.Control)
	ass.Equal(uint16(0x1234), data.Destination.SusyID)
	ass.Equal(uint32(0x12345678), data.Destination.SerialNumber)

	ass.Equal(uint8(0x12), data.JobNumber)
	ass.Equal(uint16(0x1234), data.Source.SusyID)
	ass.Equal(uint32(0x12345678), data.Source.SerialNumber)

	ass.Equal(uint16(0x1234), data.Status)
	ass.Equal(uint16(0x2143), data.PacketCount)
	ass.Equal(uint16(0x0123), data.PacketID)
	ass.Equal(uint8(0x12), data.Command)
	ass.Equal(uint16(0x1234), data.Object)

	ass.Equal([]uint32{
		0x12345678,
		0x12345678,
	}, data.Parameters)

	ass.NoError(data.Read([]byte{
		0x0C,       // length
		0x12,       // control
		0x34, 0x12, // DstSusyID
		0x78, 0x56, 0x34, 0x12, // DstSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // SrcSusyID
		0x78, 0x56, 0x34, 0x12, // SrcSerialNumber
		0x00,       // unknown
		0x12,       // JobNumber
		0x34, 0x12, // Status
		0x43, 0x21, // PacketCount
		0x23, 0x81, // PacketID | 0x8000
		0x01,       // Command
		0x02,       // Parameter count
		0x34, 0x12, // Object
		0x78, 0x56, 0x34, 0x12, // parameter 1
		0x78, 0x56, 0x34, 0x12, // parameter 2

		// empty response value
		0x12,
		0x34, 0x12,
		0x12,
		0x78, 0x56, 0x34, 0x12,

		0x00, 0x00, 0x00, 0x00,
	}))

	ass.Equal(uint8(0x12), data.Control)
	ass.Equal(uint16(0x1234), data.Destination.SusyID)
	ass.Equal(uint32(0x12345678), data.Destination.SerialNumber)

	ass.Equal(uint8(0x12), data.JobNumber)
	ass.Equal(uint16(0x1234), data.Source.SusyID)
	ass.Equal(uint32(0x12345678), data.Source.SerialNumber)

	ass.Equal(uint16(0x1234), data.Status)
	ass.Equal(uint16(0x2143), data.PacketCount)
	ass.Equal(uint16(0x0123), data.PacketID)
	ass.Equal(uint8(0x01), data.Command)
	ass.Equal(uint16(0x1234), data.Object)

	ass.Equal([]uint32{
		0x12345678,
		0x12345678,
	}, data.Parameters)

	ass.Len(data.ResponseValues, 1)
	ass.Equal(uint8(0x12), data.ResponseValues[0].Class)
	ass.Equal(uint16(0x1234), data.ResponseValues[0].Code)
	ass.Equal(uint8(0x12), data.ResponseValues[0].Type)
	ass.Equal(uint32(0x12345678), data.ResponseValues[0].Timestamp)
}

